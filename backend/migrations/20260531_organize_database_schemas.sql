CREATE TABLE IF NOT EXISTS app_auth.users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  first_name TEXT NOT NULL,
  last_name TEXT NOT NULL DEFAULT '',
  email TEXT NOT NULL,
  phone TEXT NOT NULL DEFAULT '',
  password TEXT NOT NULL,
  preferred_currency VARCHAR(10) DEFAULT 'USD',
  is_deleted BOOLEAN DEFAULT false,
  reset_token TEXT,
  reset_token_expiry TIMESTAMP,
  name TEXT,
  email_normalized TEXT,
  email_original TEXT,
  password_hash TEXT,
  mobile_normalized TEXT,
  mobile_verified_at TIMESTAMPTZ,
  email_verified_at TIMESTAMPTZ,
  account_type TEXT NOT NULL DEFAULT 'manual',
  status TEXT NOT NULL DEFAULT 'active',
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW(),
  last_login_at TIMESTAMPTZ,
  CONSTRAINT users_status_check CHECK (status IN ('active', 'suspended', 'deleted'))
);

CREATE UNIQUE INDEX IF NOT EXISTS users_email_normalized_unique
  ON app_auth.users(email_normalized)
  WHERE status <> 'deleted' AND email_normalized IS NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS users_mobile_normalized_unique
  ON app_auth.users(mobile_normalized)
  WHERE mobile_normalized IS NOT NULL AND status <> 'deleted';

CREATE INDEX IF NOT EXISTS users_status_idx ON app_auth.users(status);

INSERT INTO app_auth.users (
  id,
  first_name,
  last_name,
  email,
  phone,
  password,
  preferred_currency,
  is_deleted,
  reset_token,
