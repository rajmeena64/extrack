CREATE TABLE IF NOT EXISTS app_auth.refresh_tokens (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL REFERENCES app_auth.users(id) ON DELETE CASCADE,
  token TEXT NOT NULL,
  expires_at TIMESTAMP NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  token_hash TEXT,
  revoked_at TIMESTAMPTZ,
  user_agent TEXT,
  ip_address TEXT
);

CREATE UNIQUE INDEX IF NOT EXISTS refresh_tokens_token_hash_unique
  ON app_auth.refresh_tokens(token_hash)
  WHERE token_hash IS NOT NULL;

CREATE INDEX IF NOT EXISTS refresh_tokens_user_active_idx
  ON app_auth.refresh_tokens(user_id)
  WHERE revoked_at IS NULL;

INSERT INTO app_auth.refresh_tokens (
  id, user_id, token, expires_at, created_at, token_hash, revoked_at, user_agent, ip_address
)
