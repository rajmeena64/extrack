CREATE TABLE IF NOT EXISTS app.user_settings (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL REFERENCES app_auth.users(id) ON DELETE CASCADE,
  settings JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (user_id)
);

INSERT INTO app.user_settings (id, user_id, settings, created_at, updated_at)
SELECT id, user_id, COALESCE(settings, '{}'::jsonb), created_at, updated_at
FROM public.user_settings
WHERE EXISTS (
  SELECT 1
  FROM app_auth.users u
  WHERE u.id = public.user_settings.user_id
)
ON CONFLICT (id) DO UPDATE SET
  user_id = EXCLUDED.user_id,
  settings = EXCLUDED.settings,
  created_at = EXCLUDED.created_at,
  updated_at = EXCLUDED.updated_at;

